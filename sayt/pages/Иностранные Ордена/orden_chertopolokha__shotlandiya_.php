                        Орден Чертополоха, основанный в 1687 году королём Яковом VII Шотландским, является одним из самых древних рыцарских орденов в мире. 
Существует несколько версий происхождения ордена, среди которых есть мифические и реальные. Например, что он был создан как ответ на необходимость укрепления шотландской идентичности и монархии в условиях политической нестабильности. Первоначально он был задуман как орден для рыцарей, которые служили короне и защищали шотландские земли.

Символ чертополоха, выбранный в качестве эмблемы ордена, имеет свои корни в легенде, согласно которой шотландские воины, нападая на лагерь, наступили на колючий чертополох, что привлекло внимание защитников и дало им возможность предотвратить нападение. Этот эпизод стал символом стойкости и защиты родины.

Сегодня награда вручается лично монархом за особые заслуги перед государством. Посвящение в рыцари происходит раз в два года в Эдинбургской часовне Чертополоха — официальном храме организации.
